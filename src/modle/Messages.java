package modle;

public class Messages {


	
	   private String PMessages_Num;
	   private String PMessages_Name;
	   private String PMessages_Path;
	   private String PMessages_University;
	   private String PMessages_Specialty;
	   private String PMessages_Introduce;
	   private int PMessages_Rating;
	   private long PMessages_NumVote;
	public String getPMessages_Num() {
		return PMessages_Num;
	}
	public void setPMessages_Num(String pMessages_Num) {
		PMessages_Num = pMessages_Num;
	}
	public String getPMessages_Name() {
		return PMessages_Name;
	}
	public void setPMessages_Name(String pMessages_Name) {
		PMessages_Name = pMessages_Name;
	}
	public String getPMessages_Path() {
		return PMessages_Path;
	}
	public void setPMessages_Path(String pMessages_Path) {
		PMessages_Path = pMessages_Path;
	}
	public String getPMessages_University() {
		return PMessages_University;
	}
	public void setPMessages_University(String pMessages_University) {
		PMessages_University = pMessages_University;
	}
	public String getPMessages_Specialty() {
		return PMessages_Specialty;
	}
	public void setPMessages_Specialty(String pMessages_Specialty) {
		PMessages_Specialty = pMessages_Specialty;
	}
	public String getPMessages_Introduce() {
		return PMessages_Introduce;
	}
	public void setPMessages_Introduce(String pMessages_Introduce) {
		PMessages_Introduce = pMessages_Introduce;
	}
	public int getPMessages_Rating() {
		return PMessages_Rating;
	}
	public void setPMessages_Rating(int pMessages_Rating) {
		PMessages_Rating = pMessages_Rating;
	}
	public long getPMessages_NumVote() {
		return PMessages_NumVote;
	}
	public void setPMessages_NumVote(long pMessages_NumVote) {
		PMessages_NumVote = pMessages_NumVote;
	}
	public Messages(String pMessages_Num, String pMessages_Name, String pMessages_Path, String pMessages_University,
			String pMessages_Specialty, String pMessages_Introduce, int pMessages_Rating, long pMessages_NumVote) {
		super();
		PMessages_Num = pMessages_Num;
		PMessages_Name = pMessages_Name;
		PMessages_Path = pMessages_Path;
		PMessages_University = pMessages_University;
		PMessages_Specialty = pMessages_Specialty;
		PMessages_Introduce = pMessages_Introduce;
		PMessages_Rating = pMessages_Rating;
		PMessages_NumVote = pMessages_NumVote;
	}

	
	   
	   
	   
}


